-- ── port_arrivals: detected entry/exit events ────────────────────────────────
CREATE TABLE IF NOT EXISTS public.port_arrivals (
    id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    imo         text,
    mmsi        text,
    vessel_name text,
    port_slug   text NOT NULL REFERENCES public.port_polygons(slug),
    entered_at  timestamptz NOT NULL,
    exited_at   timestamptz,
    detected_at timestamptz DEFAULT now()
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_pa_open
    ON public.port_arrivals (imo, port_slug) WHERE exited_at IS NULL;
CREATE INDEX IF NOT EXISTS idx_pa_port_time
    ON public.port_arrivals (port_slug, entered_at DESC);

ALTER TABLE public.port_arrivals ENABLE ROW LEVEL SECURITY;
CREATE POLICY "acesso autenticado" ON public.port_arrivals
    FOR SELECT TO authenticated USING (true);

-- ── RPC: latest AIS position for each vessel in a given navios_diesel snapshot
CREATE OR REPLACE FUNCTION public.get_ais_positions_latest(p_collected_at timestamptz)
RETURNS json LANGUAGE sql SECURITY DEFINER AS $$
  WITH navios AS (
    SELECT DISTINCT navio, imo, mmsi
    FROM public.navios_diesel
    WHERE collected_at = p_collected_at
  ),
  resolved AS (
    -- prefer explicit imo on the row; otherwise look up by normalised name
    SELECT
      n.navio,
      COALESCE(n.imo, r.imo)   AS imo,
      COALESCE(n.mmsi, r.mmsi) AS mmsi
    FROM navios n
    LEFT JOIN public.vessel_registry r
      ON r.name_norm = upper(regexp_replace(n.navio, '[^A-Za-z0-9]', '', 'g'))
  ),
  latest AS (
    SELECT DISTINCT ON (vp.imo, vp.mmsi)
      vp.imo, vp.mmsi, vp.ts, vp.lat, vp.lon, vp.sog, vp.cog, vp.nav_status, vp.inside_port
    FROM public.vessel_positions vp
    WHERE vp.ts >= now() - interval '7 days'
    ORDER BY vp.imo, vp.mmsi, vp.ts DESC
  )
  SELECT COALESCE(json_agg(row_to_json(x)), '[]'::json) FROM (
    SELECT
      r.navio, r.imo, r.mmsi,
      l.ts, l.lat, l.lon, l.sog, l.cog, l.nav_status, l.inside_port
    FROM resolved r
    LEFT JOIN latest l
      ON (r.imo IS NOT NULL AND l.imo = r.imo)
      OR (r.imo IS NULL AND r.mmsi IS NOT NULL AND l.mmsi = r.mmsi)
    WHERE l.lat IS NOT NULL
  ) x;
$$;

-- ── RPC: open port arrivals (vessels currently inside a port polygon) ────────
CREATE OR REPLACE FUNCTION public.get_ais_arrivals_open()
RETURNS json LANGUAGE sql SECURITY DEFINER AS $$
  SELECT COALESCE(json_agg(row_to_json(x) ORDER BY x.entered_at DESC), '[]'::json) FROM (
    SELECT
      pa.imo,
      pa.mmsi,
      COALESCE(vr.name, pa.vessel_name) AS vessel_name,
      pa.port_slug,
      pp.name AS port_name,
      pa.entered_at,
      pa.exited_at,
      pa.detected_at
    FROM public.port_arrivals pa
    LEFT JOIN public.vessel_registry vr ON vr.imo = pa.imo
    LEFT JOIN public.port_polygons pp ON pp.slug = pa.port_slug
    WHERE pa.exited_at IS NULL
  ) x;
$$;

-- ── RPC: all port polygons (GeoJSON) ─────────────────────────────────────────
CREATE OR REPLACE FUNCTION public.get_port_polygons()
RETURNS json LANGUAGE sql SECURITY DEFINER AS $$
  SELECT COALESCE(json_agg(row_to_json(x) ORDER BY x.slug), '[]'::json) FROM (
    SELECT slug, name, polygon FROM public.port_polygons
  ) x;
$$;
