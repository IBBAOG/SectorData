-- ── Extend navios_diesel with AIS identifiers ────────────────────────────────
ALTER TABLE public.navios_diesel
    ADD COLUMN IF NOT EXISTS imo  text,
    ADD COLUMN IF NOT EXISTS mmsi text;

CREATE INDEX IF NOT EXISTS idx_nd_imo  ON public.navios_diesel (imo);
CREATE INDEX IF NOT EXISTS idx_nd_mmsi ON public.navios_diesel (mmsi);

-- ── vessel_registry: canonical name ↔ IMO ↔ MMSI ─────────────────────────────
CREATE TABLE IF NOT EXISTS public.vessel_registry (
    imo           text PRIMARY KEY,
    mmsi          text,
    name          text NOT NULL,
    name_norm     text GENERATED ALWAYS AS (upper(regexp_replace(name, '[^A-Za-z0-9]', '', 'g'))) STORED,
    ship_type     text,
    flag          text,
    last_seen_at  timestamptz,
    first_seen_at timestamptz DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_vr_name_norm ON public.vessel_registry (name_norm);
CREATE INDEX IF NOT EXISTS idx_vr_mmsi      ON public.vessel_registry (mmsi);

ALTER TABLE public.vessel_registry ENABLE ROW LEVEL SECURITY;
CREATE POLICY "acesso autenticado" ON public.vessel_registry
    FOR SELECT TO authenticated USING (true);

-- ── vessel_positions: time-series of AIS positions ───────────────────────────
CREATE TABLE IF NOT EXISTS public.vessel_positions (
    id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    imo         text,
    mmsi        text,
    ts          timestamptz NOT NULL,
    lat         double precision NOT NULL,
    lon         double precision NOT NULL,
    sog         double precision,
    cog         double precision,
    nav_status  text,
    inside_port text,
    CONSTRAINT vp_identifier_present CHECK (imo IS NOT NULL OR mmsi IS NOT NULL)
);

CREATE INDEX IF NOT EXISTS idx_vp_imo_time  ON public.vessel_positions (imo, ts DESC);
CREATE INDEX IF NOT EXISTS idx_vp_mmsi_time ON public.vessel_positions (mmsi, ts DESC);
CREATE INDEX IF NOT EXISTS idx_vp_inside    ON public.vessel_positions (inside_port) WHERE inside_port IS NOT NULL;

ALTER TABLE public.vessel_positions ENABLE ROW LEVEL SECURITY;
CREATE POLICY "acesso autenticado" ON public.vessel_positions
    FOR SELECT TO authenticated USING (true);

-- ── port_polygons: bounding polygon for each monitored port ──────────────────
CREATE TABLE IF NOT EXISTS public.port_polygons (
    slug       text PRIMARY KEY,
    name       text NOT NULL,
    polygon    jsonb NOT NULL,
    updated_at timestamptz DEFAULT now()
);

ALTER TABLE public.port_polygons ENABLE ROW LEVEL SECURITY;
